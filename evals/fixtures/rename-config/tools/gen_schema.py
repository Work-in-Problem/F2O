#!/usr/bin/env python3
"""Regenerate generated/config_schema.py from relay.spec.SPEC.

The generated module is the dependency-free schema consumed by ops tooling;
relay.config refuses to load when it drifts from SPEC. Run this script (or
``make schema``) after ANY change to SPEC — never edit the generated file
by hand.
"""

import hashlib
import os
import sys

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, ROOT)

from relay.spec import SPEC, TYPE_NAMES  # noqa: E402

OUT_PATH = os.path.join(ROOT, "generated", "config_schema.py")

HEADER = (
    "# AUTO-GENERATED by tools/gen_schema.py — DO NOT EDIT BY HAND.\n"
    "# Source of truth: relay/spec.py (SPEC). Regenerate with: make schema\n"
    '"""Published config schema for relaybox (machine-written)."""\n'
)


def main():
    fields = {key: TYPE_NAMES[entry["type"]] for key, entry in SPEC.items()}
    names = sorted(fields)
    blob = ";".join("{}:{}".format(name, fields[name]) for name in names)
    fingerprint = hashlib.sha256(blob.encode("utf-8")).hexdigest()[:16]

    lines = [HEADER, "SCHEMA_VERSION = 1", "", "FIELDS = {"]
    for name in names:
        lines.append('    "{}": "{}",'.format(name, fields[name]))
    lines.append("}")
    lines.append("")
    lines.append('FINGERPRINT = "{}"'.format(fingerprint))
    lines.append("")

    with open(OUT_PATH, "w", encoding="utf-8") as handle:
        handle.write("\n".join(lines))
    print(
        "wrote {} ({} fields, fingerprint {})".format(
            os.path.relpath(OUT_PATH, ROOT), len(names), fingerprint
        )
    )


if __name__ == "__main__":
    main()
